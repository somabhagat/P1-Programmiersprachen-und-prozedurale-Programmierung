#include <stdio.h>
#include <stdlib.h>

int main()
{
    // Deklaration und Initialisierung der char-Variablen zeichen.
    char Zeichen = 'd';

    // Deklaration und Initialisierung der int-Variablen position.
    int position = 4;

    // Ausgabe einer Nachricht mit der Funktion printf().
    printf("Wichtige Mitteilung: %c ist der %d Buchstabe im Alphabet.\n", Zeichen, position);
}
